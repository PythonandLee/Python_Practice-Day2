# In Python, [] is a list.
# []'s index starts from '0', so if want to take value from 
# a specific position, remember to -1(minus 1)

# If want to take the last value, just simply use [-1]
# The one before last one [-2], the one before last two [-3], and so on...

color = ['black', 'red', 'green', 'purple']

print(color)
print(color[2].title())

# Taking the last item
print(color[-1])